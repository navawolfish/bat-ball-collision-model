def mps_to_mph(mps):
    """Converts m/s to miles per hour."""
    return mps * 2.23694

def m_to_inches(m):
    """Converts metres to inches"""
    return m * 39.37

def mph_to_mps(mph):
    """Converts miles per hour to m/s"""
    return mph / 2.23694

def inches_to_m(inches):
    """Converts inches to metres"""
    return inches / 39.37

def kg_to_oz(kg):
    """Converts kg to ounces"""
    return kg * 35.274

def oz_to_kg(oz):
    """Converts ounces to kg"""
    return oz / 35.274
